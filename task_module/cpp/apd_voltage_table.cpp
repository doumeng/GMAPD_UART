float getSpiAndLevelByVoltage(float targetVoltage, uint16_t& outSpi, uint8_t& outLevel) {
    const int tableSize = 1024;
    if (tableSize == 0) return 0.0f; // 安全防护

    float globalMinVoltage = 99999.0f;
    float globalMaxVoltage = -99999.0f;
    int minIdx = 0;
    int maxIdx = 0;

    // 寻找真实物理界限
    for (int i = 0; i < tableSize; ++i) {
        if (configTable[i].voltage < globalMinVoltage) {
            globalMinVoltage = configTable[i].voltage;
            minIdx = i;
        }
        if (configTable[i].voltage > globalMaxVoltage) {
            globalMaxVoltage = configTable[i].voltage;
            maxIdx = i;
        }
    }

    // 1. 如果比系统下限还要低
    if (targetVoltage <= globalMinVoltage) {
        outSpi = configTable[minIdx].spiValue;
        outLevel = configTable[minIdx].level;
        return configTable[minIdx].voltage;
    }
    
    // 2. 如果比系统上限还要高
    if (targetVoltage >= globalMaxVoltage) {
        outSpi = configTable[maxIdx].spiValue;
        outLevel = configTable[maxIdx].level;
        return configTable[maxIdx].voltage;
    }

    // 3. 正常遍历寻找最近邻的值 (绝对距离最小)
    float minDiff = 99999.0f;
    int bestIdx = 0;
    for (int i = 0; i < tableSize; ++i) {
        float diff = std::abs(configTable[i].voltage - targetVoltage);
        if (diff < minDiff) {
            minDiff = diff;
            bestIdx = i;
        }
    }

    outSpi = configTable[bestIdx].spiValue;
    outLevel = configTable[bestIdx].level;
    return configTable[bestIdx].voltage;
}

void interactiveVoltageTest() {
    std::cout << "--- 开启电压转SPI测试模式 (输入 0 退出) ---" << std::endl;
    while(true) {
        float inputV = 0.0f;
        std::cout << "\n请输入目标电压值: ";
        if (!(std::cin >> inputV) || inputV == 0.0f) {
            std::cout << "退出测试" << std::endl;
            break;  // 输入0或非数字退出
        }
        
        uint16_t outSpi = 0;
        uint8_t outLevel = 0;
        float actualV = getSpiAndLevelByVoltage(inputV, outSpi, outLevel);
        
        std::cout << ">> 目标电压: " << inputV << " V" << std::endl;
        std::cout << ">> 实际匹配电压: " << actualV << " V" << std::endl;
        std::cout << ">> 对应 SPI: " << outSpi << " (0x" << std::hex << outSpi << std::dec << ")" << std::endl;
        std::cout << ">> 对应 Level(Value): " << static_cast<int>(outLevel) << std::endl;
    }
}

